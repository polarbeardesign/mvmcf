<h1>Cornerstone Club - Quarterly Giving</h1>

<h2>The foundation of every life we touch.</h2>

<p>Madison Valley Medical Center Foundation's definition of a Cornerstone Club member is one who supports the Foundation by making an annual commitment to contribute quarterly to insure a regular source of funds in support of the Medical Center's mission.</p>

<p>One of the challenges to running a successful not-for-profit Medical Center is to ensure that there is adequate resources year round to support the operation to make sure that the highest-quality of health care is available in the community.</p>

<p>Through quarterly donations, Cornerstone Club members provide the Madison Valley Medical Center Foundation with a consistent and reliable source of funding that allows us to plan ahead for the needs of the Medical Center.</p>

<h2>What is your gift equivalent to?</h2>

<ul>
  <li>$50 - meals for patients for a day</li>
    <li>$100 - telephone service for a day</li>
    <li>$250 - Lab supplies for a day</li>
    <li>$500 - CT scan services for a day</li>
    <li>$750 - drugs for patients for a day</li>
    <li>$1000 - medical supplies for two days or heating/cooling the Medical Center for three days.</li>
</ul>

<h2>Why should you join the Cornerstone Club?</h2>

<p>There are no club meetings. You won't have to pass an initiation. There are no secret handshakes. You won't have to wear a funny hat. You won't have to join a committee.</p>

<ul>
  <li>You may enjoy hassle-free quarterly donations. </li>
  <li>You may manage automatic donations from your credit card.</li>
  <li>Your donation is put to work immediately in the community.</li>
  <li>Your donation is tax deductible.</li>
</ul>

