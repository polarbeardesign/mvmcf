<h1>The Madison Valley Medical Center</h1>

<img src="photos/mvmc_spring_sm.jpg" width="161" height="98" alt="The Madison Valley Medical Center" style="margin: 0 12%;"  />

<p>The beautiful new facility is filled with State-of-the-Art Technology. Patients have the comfort of private rooms and private baths, most rooms also enjoy beautiful views of the valley and surrounding mountains.</p>

<p>The building is a two-level, 36,470 square foot medical facility with ten acute care hospital beds, a full laboratory, imaging department, emergency room and a nine exam room clinic and a physical therapy department.</p>
