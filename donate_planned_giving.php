    <h1>Planned Giving</h1>

    <h2>What is a planned gift?</h2>

    <p>A planned gift may take the form of an estate gift or life-income gift.</p>

    <p>Examples of planned gifts include:</p>

    <ul>
      <li>Naming The Foundation in your estate plan (will or trust)</li>
      <li>Naming The Foundation as a beneficiary of a life insurance policy or a retirement plan</li>
      <li>Gifts of stock, real estate, or securities</li>
    </ul>

    <p>Planned Giving means finding a way to make an important gift to the Medical Center Foundation, a cause you believe in, while still receiving the best tax benefits possible and achieving personal financial goals. Your generosity and thoughtfulness will provide you and your family a sense of satisfaction and pride in being able to make a difference at the Madison Valley Medical Center Foundation.</p>

    <p>Leaving a gift to The Foundation in your will is easy, you may designate a certain dollar amount or you may leave a percentage of your estate. You can also bequeath a specific piece of property like a home, securities (for instance, stocks or mutual funds) or other financial assets.</p>

    <h2>Sample Will Language</h2>

    <p>You may find it helpful to have sample will language to share with an attorney:</p>

    <p class="indent">"I give, devise, and bequeath to the Madison Valley Medical Center Foundation, a non-profit corporation, located at 305 N. Main Ennis, MT 59729, the sum of $______(or,____ percent of my estate, or, ____ percent of the residue of my estate) to be used for such purposes as the Madison Valley Medical Center Foundation Board of Trustees determines at the time this bequest becomes effective."</p>

    <h2>Designating Your Gift for a Specific Purpose</h2>

    <p>Will or trust provisions may be designated for a specific purpose - such as a specific department or project in the Medical Center. If you wish to restrict or designate a use for your gift, please contact the Foundation Office to ensure that we understand your wishes.</p>

    <p>In the case of a restricted bequest, you may wish to add language that would give the Foundation the flexibility to use the bequest for some other purpose in the event that the project or purpose designated by the donor is no longer needed when the funds become available. Adding the following sentence to the end of the bequest provision would provide this flexibility:</p>

    <p class="indent">"If, at the time this bequest becomes effective, the funds are not needed for this purpose, then the Madison Valley Medical Center Foundation Board of Trustees may direct the funds to be used for a purpose as closely related as possible to the original purpose described in this provision."</p>

    <h2>Your Generosity Today will Continue to Help Others Tomorrow</h2>

    <p>By making a gift in your will, your generosity will continue to help others, ensuring that you are helping to provide the best healthcare possible for the residents of Madison County.</p>

    <p>By hearing about your gift intentions, the Foundation may better prepare to receive your future gift and say a heartfelt "thank you" today.</p>

