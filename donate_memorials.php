<h1>Honorary &amp; Memorial Donations</h1>

<p>If you have a family member or friend whose life has been touched by the Madison Valley Medical Center, we hope you'll consider making a gift to the Foundation in honor of that person. Providing a gift establishes a living tribute that allows you to:</p>

<ul>
  <li>honor a loved one or yourself;</li>
  <li>support our continuing efforts; and receive personal financial benefits from your contribution.</li>
</ul>

<h2>Giving Now or Later</h2>

<p>Any charitable gift you arrange can be made in honor of someone. Consider these two basic methods of establishing a memorial or honorary gift.</p>

<p>A gift today &#8211; An outright gift can help fund the Medical Center's immediate needs or an upcoming project. The financial benefits include an income tax deduction and possible elimination of capital gains tax.</p>

<p>A gift through your will &#8211; You can include a bequest in your will, stating that a specific asset, certain dollar amount or &#8211; more commonly &#8211; a percentage of your estate will pass to the Medical Center Foundation at your death in honor of your loved one.</p>

<h2>Next Step</h2>

<p>Making a memorial or honorary gift is a wonderful way to acknowledge someone's vision for the future. Depending on the nature of your gift, there are a variety of ways we can recognize your generosity. Please contact the Foundation to learn more about ways to recognize your honoree.</p>

