<h1>Annual Reports &amp; 990 Forms</h1>

<p>Although  not-for-profit entities are generally exempt from paying federal income tax,  they are not free from filing requirements with the Internal Revenue Service.  Not-for-profits that normally have gross annual receipts of more than $25,000  must file a Form 990 with the IRS within 5 months of the close of the fiscal  year.</p>

<p>Form  990 contains information relative to revenue, expenses, assets, liabilities and  fund balance. It includes statement of mission, description of significant  activities and program services, disclosure of compensation, relationships and  transactions of board members and key employees, and information on governance  practices.</p>

<p>The IRS requires the Form 990 be made available to the public</p>

